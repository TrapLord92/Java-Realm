package com.traplord.oop.methods01;

public class IntroIntoMethods {
    public static void main(String[] args){
        //Calling the method
        saiHi("John");
        //Calling the method again
        saiHi("Sara");

    }//end of the main method

    public static void saiHi(String name){
        System.out.println("Hello there my friend " + name);

    }
}//end of the class
