package com.traplord.moredata_struture;

public class TwoDArrays {
public static void main(String[] args){
    //declaring 2d arrays
    int arr[][]={{2,7,9},{3,6,1},{7,4,2}};

//    printing 2d arrays

    for(int i=0;i<3;i++){
        for(int j=0;j<3;j++){
            System.out.print(arr[i][j]+" ");
        }
        System.out.println();
    }
}
}
